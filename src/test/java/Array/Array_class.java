package Array;

public class Array_class {
    public static void main(String[] args) {
        String f[]=new String[4];
        f[0]="apple";
        f[1]="banana";
        f[2]="mango";
        f[3]="grapes";

        System.out.println(f[0]);

        for(String S:f){
            System.out.println(S);

        }




    }
}
